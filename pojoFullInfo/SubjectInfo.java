package pojoFullInfo;

public class SubjectInfo {
	
	private int id;
	private String name;
	private String sname;
	private String count;
	private String islect;
	private String idteach;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getSname() {
		return sname;
	}
	public void setSname(String sname) {
		this.sname = sname;
	}
	public String getCount() {
		return count;
	}
	public void setCount(String count) {
		this.count = count;
	}
	public String getIslect() {
		return islect;
	}
	public void setIslect(String islect) {
		this.islect = islect;
	}
	public String getIdteach() {
		return idteach;
	}
	public void setIdteach(String idteach) {
		this.idteach = idteach;
	}
	
	
	

}
